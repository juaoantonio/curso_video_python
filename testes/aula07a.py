# Aula #07 Operadores Aritméticos

n1 = int(input('Um valor: '))
n2 = int(input('Outro valor: '))
soma = n1 +n2
sub = n1-n2
d = n1/n2
mult = n1*n2
print(f'Entre esses números, a soma é {soma}, a subtração é {sub},', end='')
# end= serve para não 'quebrar' a linha
# \n 'quebra' a linha de print
print( f' a divisão é {d:.3f} e a multiplicação é {mult}')

